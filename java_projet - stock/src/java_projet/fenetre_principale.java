    public ArrayList<Produit> getProductList()
    {
            ArrayList<Produit> produitList = new ArrayList<Produit>();
            Connection con = getConnection();
            String query = "SELECT * FROM produits";
            
            Statement st;
            ResultSet rs;
        try {
           
            st = con.createStatement();
            rs = st.executeQuery(query);
            Produit produit;
            
            while(rs.next())
            {
                produit = new Produit(rs.getInt("id"),rs.getString("nom"), Float.parseFloat(rs.getString("prix")),rs.getBytes("image"),rs.getInt("quantite"));
                produitList.add(produit);
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(fenetre_principale.class.getName()).log(Level.SEVERE, null, ex);
        }
        return produitList;
    }
    
    
