    public void montrer_produits_dans_JTable()
    {
        ArrayList<Produit> list = getProductList();
        DefaultTableModel model = (DefaultTableModel)JTable_Produits.getModel();
        // Effacer le contenu de la JTable
        model.setRowCount(0);
        Object[] row = new Object [4];
        for(int i = 0; i < list.size(); i++)
        {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getNom();
            row[2] = list.get(i).getPrix();
            row[3] = list.get(i).getQuantite();
            
            model.addRow(row);
        }
    }
    
