    private void btn_supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_supprimerActionPerformed
        if(!txt_id.getText().equals(""))
        {
            try {
                Connection con = getConnection();
                PreparedStatement ps = con.prepareStatement("DELETE FROM produits WHERE id = ?");
                int id = Integer.parseInt(txt_id.getText());
                ps.setInt(1, id);
                ps.executeUpdate();
                montrer_produits_dans_JTable();
                JOptionPane.showMessageDialog(null, "Produit supprimé");
            } catch (SQLException ex) {
                Logger.getLogger(fenetre_principale.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Erreur : Produit non supprimé");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Erreur : Produit non supprimé, pas d'ID à supprimer");
        }
    }//GEN-LAST:event_btn_supprimerActionPerformed

